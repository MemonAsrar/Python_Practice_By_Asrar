age = int(input("Enter your Age in years: "))

months = age * 12
weeks = age * 52
days = age * 365
hours = days * 24
minutes = hours * 60
seconds = minutes * 60

print(f"Total Months = {months}")
print(f"Total Weeks  = {weeks}")
print(f"Total Days   = {days}")
print(f"Total Hours  = {hours}")
print(f"Total Minutes= {minutes}")
print(f"Total Seconds= {seconds}")
